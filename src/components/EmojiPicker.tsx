type CategoryKey = keyof typeof EMOJI_CATEGORIES;

interface EmojiPickerProps {
  onSelect: (emoji: string) => void;
  onClose: () => void;
}

export default function EmojiPicker({ onSelect, onClose }: EmojiPickerProps) {
  const [activeCategory, setActiveCategory] = useState<CategoryKey>('Smileys');
  const [search, setSearch] = useState('');
  const pickerRef = useRef<HTMLDivElement>(null);

  // Close on click outside
  useEffect(() => {
    function handleClickOutside(e: MouseEvent) {
      if (pickerRef.current && !pickerRef.current.contains(e.target as Node)) {
        onClose();
      }
    }
    document.addEventListener('mousedown', handleClickOutside);
    return () => document.removeEventListener('mousedown', handleClickOutside);
  }, [onClose]);

  // Filter emojis by search
  const filteredEmojis = search
    ? Object.values(EMOJI_CATEGORIES).flat().filter((e) => e.includes(search) || e === search)
    : EMOJI_CATEGORIES[activeCategory];

  return (
    <div className="emoji-picker" ref={pickerRef}>
      <div className="emoji-picker-header">
        <input
          type="text"
          placeholder="Search emoji..."
          value={search}
          onChange={(e) => setSearch(e.target.value)}
          className="emoji-search"
        />
        <button className="emoji-close-btn" onClick={onClose}>
          <span className="material-icons">close</span>
        </button>
      </div>
      {!search && (
        <div className="emoji-categories">
          {(Object.keys(EMOJI_CATEGORIES) as CategoryKey[]).map((cat) => (
            <button
              key={cat}
              className={`emoji-category-btn ${activeCategory === cat ? 'active' : ''}`}
              onClick={() => setActiveCategory(cat)}
            >
              {cat}
            </button>
          ))}
        </div>
      )}
      <div className="emoji-grid">
        {filteredEmojis.map((emoji, i) => (
          <button
            key={`${emoji}-${i}`}
            className="emoji-btn"
            onClick={() => onSelect(emoji)}
          >
            {emoji}
          </button>
        ))}
      </div>
    </div>
  );
}
